package com.icaro.clase2;

public class A1HolaMundo {

    public static void main(String[] args) {

        System.out.println("Hola mundo!"); //String
        System.out.println(10);            //Entero
        System.out.println(20.56);         //Flotante
        System.out.println(true);          //Booleano
        System.out.println('C');           //Caracter o Char

        System.out.println("Hola " + "Mundo, hoy es: " + 31);

    }

}
